#include "libft.h"
#include <stdlib.h>

char	*ft_strdup(const char *s)
{
	char	*str;
	char	*p;
	int		length;

	length = 0;
	while (s[length])
	{
		length++;
	}
	str = malloc(length + 1);
	if (str == NULL)
		return (NULL);
	p = str;
	while (*s)
	{
		*p = *s;
		p++;
		s++;
	}
	*p = '\0';
	return (str);
}
